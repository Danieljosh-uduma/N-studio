export function escapeHTML(str) {
    return String(str)
        .replace(/&/g, "&amp;")
        .replace(/</g, "&lt;")
        .replace(/>/g, "&gt;")
        .replace(/"/g, "&quot;")
        .replace(/'/g, "&#39;")
        .replace(/\//g, "&#x2F;")
        .replace(/`/g, "&#96;")
        .replace(/=/g, "&#61;")
        .replace(/\{/g, "&#123;")
        .replace(/\}/g, "&#125;")
        .replace(/\$/g, "&#36;")
        .replace(/\(/g, "&#40;")
        .replace(/\)/g, "&#41;")
        .replace(/\[/g, "&#91;")
        .replace(/\]/g, "&#93;")
        .replace(/!/g, "&#33;")
        .replace(/@/g, "&#64;")
        .replace(/#/g, "&#35;")
        .replace(/%/g, "&#37;")
        .replace(/\*/g, "&#42;")
        .replace(/:/g, "&#58;")
        .replace(/;/g, "&#59;")
        .replace(/,/g, "&#44;")
        .replace(/\./g, "&#46;")
        .replace(/\^/g, "&#94;")
        .replace(/_/g, "&#95;")
        .replace(/\|/g, "&#124;")
        .replace(/\\/g, "&#92;");
}